<?php
//Создайте массив вида [1, [2], [[3]], [[[4]]] ], вместо 4 может быть любое целое значение.
// К примеру, если 5 – тогда будет массив [1, [2], [[3]], [[[4]]], [[[[5]]]] ].


$array = [1, [2], [[3]], [[[4]]]];
$maxValue = 5;

for ($i = 1; $i <= $maxValue; $i++) {
    $nestedArray = [];
    for ($j = 0; $j < $i; $j++) {
        $nestedArray[] = $i;
    }
    $array[] = $nestedArray;
}

echo json_encode($array);

